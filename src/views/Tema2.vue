      h2 2.2 Derechos de autor
    p.mb-5 El derecho de autor es la protección que le otorga el Estado al creador de las obras literarias o artísticas desde el momento de su creación y por un tiempo determinado (DNDA, s.f.). Los derechos de autor y conexos, son los que protegen las obras literarias, artísticas y científicas; y los derechos conexos que abarcan los derechos de los artistas, intérpretes productores, organismos de radiodifusión, relacionados con las interpretaciones o ejecuciones.
    h4 Derechos patrimoniales y morales
    p.mb-5 Según la Ley Nº 23 de 1982, en el artículo 72. El derecho patrimonial del autor se causa desde el momento en que la obra o producción, susceptible de estimación económica y cualquiera que sea su finalidad, se divulgue por cualquier forma o modo de expresión. Artículo 73. En todos los casos en que los autores o las asociaciones de autores, celebren contratos con los usuarios o con las organizaciones que los representen, respecto al derecho de autor, por concepto de ejecución, representación, exhibición y, en general, por uso o explotación de las obras protegidas por la presente ley, serán las tarifas concertadas en los respectivos contratos, las que tendrán aplicación, siempre que no sean contrarias a los principios consagrados por la misma.
